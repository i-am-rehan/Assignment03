"use strict";
// This line declares a variable num1 of type number and assigns it the value 2.
let num1 = 2;
// This line declares a variable num2 of type number and assigns it the value 10.
let num2 = 10;
// This line prints the result of adding num1 and num2 to the console.
console.log(num1 + num2);
// This line prints the result of subtracting num2 from num1 to the console.
console.log(num1 - num2);
// This line prints the result of multiplying num1 and num2 to the console.
console.log(num1 * num2);
// This line prints the result of dividing num1 by num2 to the console.
console.log(num1 / num2);
// This line prints the remainder of dividing num1 by num2 (the modulus) to the console.
console.log(num1 % num2);
